# ─────────────────────────────────────────────────────────────────────────────
#  Основные функции
# ─────────────────────────────────────────────────────────────────────────────

@lru_cache
def get_morph():
    return pymorphy3.MorphAnalyzer()


def detect_language(text: str) -> str:
    ru_count = len(re.findall(r"[а-яА-ЯёЁ]", text))
    en_count = len(re.findall(r"[a-zA-Z]", text))
    return "ru" if ru_count >= en_count else "en"


def normalize_text(text: str) -> str:
    text = (text or "").lower()
    # Декодируем HTML-сущности (&nbsp; → пробел, &amp; → &, и т.д.)
    text = html.unescape(text)
    # Сначала убираем медиа-метки (до остальной очистки)
    text = _strip_media_watermarks(text)
    text = re.sub(r"http\S+", " ", text)
    text = re.sub(r"www\.\S+", " ", text)
    text = re.sub(r"<[^>]+>", " ", text)
    text = re.sub(r"[^a-zA-Zа-яА-ЯёЁ0-9\s\-]", " ", text)
    text = re.sub(r"\s+", " ", text).strip()
    return text


def tokenize(text: str) -> list[str]:
    return re.findall(r"[a-zA-Zа-яА-ЯёЁ\-]{3,}", text.lower())


def lemmatize_ru(tokens: list[str]) -> list[str]:
    morph = get_morph()
    lemmas = []
    for token in tokens:
        parsed = morph.parse(token)
        lemmas.append(parsed[0].normal_form if parsed else token)
    return lemmas


def remove_stopwords(tokens: list[str], lang: str) -> list[str]:
    # После лемматизации фильтруем по ОБОИМ спискам:
    # русский текст может содержать английские аббревиатуры и наоборот
    combined = RU_STOPWORDS | EN_STOPWORDS if lang == "ru" else EN_STOPWORDS
    return [t for t in tokens if t not in combined]


def clean_text(text: str) -> str:
    normalized = normalize_text(text)
    if not normalized:
        return ""

    lang = detect_language(normalized)
    tokens = tokenize(normalized)

    if lang == "ru":
        tokens = lemmatize_ru(tokens)

    tokens = remove_stopwords(tokens, lang)
    return " ".join(tokens)


# ─────────────────────────────────────────────────────────────────────────────
#  Классы (интерфейс не меняется — обратная совместимость)
# ─────────────────────────────────────────────────────────────────────────────

class TextCleaner:
    def clean(self, text: str) -> str:
        return clean_text(text)

    def normalize(self, text: str) -> str:
        return normalize_text(text)

    def tokenize(self, text: str) -> list[str]:
        return tokenize(text)

    def hash_content(self, text: str) -> str:
        cleaned = self.clean(text)
        return hashlib.sha256(cleaned.encode("utf-8")).hexdigest()


class RussianTextPreprocessor:
    def clean(self, text: str) -> str:
        return clean_text(text)