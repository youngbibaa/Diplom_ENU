# ─────────────────────────────────────────────────────────────────────────────
#  Паттерны для удаления медиа-меток ДО лемматизации
#
#  Lenta.ru, RBC и ряд других изданий вставляют в начало текста строки вида:
#    "Реклама Фото: Sergei Bobylev / TASS"
#    "Фото: Reuters"
#    "© AP Photo / John Smith"
#  После лемматизации "реклама фото" попадает в топ-слова каждой темы.
# ─────────────────────────────────────────────────────────────────────────────

# Убираем "реклама фото <что угодно до конца строки или до следующего слова>".
# Паттерн ловит вариации: "Реклама фото:", "реклама. фото", кириллица + латиница агентства.
_MEDIA_WATERMARK_PATTERNS = [
    re.compile(r"реклам[аы][\s\S]{0,6}фото[^\n]{0,120}", re.IGNORECASE),
    re.compile(r"фото\s*:\s*[^\n]{0,120}", re.IGNORECASE),
    re.compile(r"©\s*[a-zа-яёa-z\s]{1,40}photo[^\n]{0,80}", re.IGNORECASE),
    re.compile(r"\bphoto\b[^\n]{0,80}", re.IGNORECASE),
]


def _strip_media_watermarks(text: str) -> str:
    """Удаляет медиа-метки (реклама фото, © Agency) из текста."""
    for pattern in _MEDIA_WATERMARK_PATTERNS:
        text = pattern.sub(" ", text)
    return text


